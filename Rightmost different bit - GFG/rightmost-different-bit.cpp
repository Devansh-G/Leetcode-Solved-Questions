class Solution
{
    public:
    //Function to find the first position with different bits.
    int posOfRightMostDiffBit(int m, int n)
    {
        // Your code here
        if(n==m)
            return -1;
        int temp = m^n;
        int ans=0;
        while(temp>0)
        {
            ans++;
            if(temp%2==1)
                break;
            temp=temp>>1;
        }
        return ans;
    }
};

